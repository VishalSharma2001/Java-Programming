package pack2;
public class Student{
 private int rollno;
 private String name;
 private int marks;
 public void setRollno(int r){
	 rollno=r;
 }
 public void setName(String n){
	 name=n;
 }
 public void setmarks(int m){
	 marks=m;
 }
 public int getRollno(){
	 return(rollno);
 }
 public String getName(){
	 return(name);
 }
 public int getmarks(){
	 return(marks);
 }
}